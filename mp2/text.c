/*
 * string_convert
 *   DESCRIPTION: convert input string data into buffer holding ASCII data of the input string
 *   INPUTS: string -- string data to be converted in to ASCII buffer
 *           buffer -- buffer that will be holding ASCII character data of the string
 *           color_background -- color of the background
 *           color_text -- color of the text
 *   OUTPUTS: none
 *   RETURN VALUE: none
 *   SIDE EFFECTS: buffer holds the information of the string after converted. And it is not ordered by plane.
 *
 */
void string_covert(const char* string, unsigned char* buffer, unsigned char color_background, unsigned char color_text){
    int i,j,k,l;
    int length = strlen(string);
    int offset = (320-length*8)/2; /*since we need to put the string in the center of the bar so we need an offset. (bar_width - number of character*character width)/2*/
    int index;
    int location;
    unsigned char temp;
    /*fill the buffer with background color 320*18 means the status bar's height is 18 and width is 320*/
    for(i = 0;i<320*18;i++){
        buffer[i] = color_background;
    }
    
    for(j = 0;j<length;j++){
        index = (int)string[j];
        for(k = 0;k<16;k++){ /*16 is the height of the character*/
            temp = font_data[index][k];
            for(l = 0;l<8;l++){ /*8 is the width of the character*/
                if(temp & (0x80 >> l)){
                    location = 320 + offset + k*320 + l + j*8; /*since the first line of the buffer is not used so first add a bar_width
                                                                location = bar_width + offset(to be the center) + height_index*bar_width + width_index + index(the number of character)*character_width*/
                    buffer[location] = color_text;
                }
            }
        }
    }
}


/*
 * fruit_string_convert
 *   DESCRIPTION: convert input string data into buffer holding ASCII data of the input string and update the color using the pragrant color
 *   INPUTS: string -- string data to be converted in to ASCII buffer
 *           fruit_text -- buffer that will be holding ASCII character data of the string
 *   OUTPUTS: none
 *   RETURN VALUE: none
 *   SIDE EFFECTS: buffer holds the information of the string after converted. And it is not ordered by plane.
 *
 */

void fruit_string_convert(const char* string, unsigned char* fruit_text){
  int i,j,k;
  int str_len = strlen(string);
  int offset = (TEXT_X - str_len*TEXT_WIDTH)/2;                                      
  unsigned char mask[TEXT_X*TEXT_Y];    
  unsigned char* current_character;                                 
  int location = 0;
  int offset_color = 0x40;      
                                                               
  //first set the mask all equal to 0 which means all pixel is background color.
  for(i = 0; i < TEXT_X*TEXT_Y ;i++){
    mask[i] = 0;
  }
// the same as above function.
  for(i = 0; i < str_len; i++){
    int font_index = (int)string[i];
    current_character = font_data[font_index];
    for(j = 0; j < TEXT_HEIGHT ; j++){
      for(k = 0; k < TEXT_WIDTH ; k++){
        location = TEXT_X + offset + j * TEXT_X + k + i * TEXT_WIDTH;
        if(current_character[j] & (0x80 >> k)) { 
          mask[location] = 1;
        }
      }
    }
  }

  for(i = 0; i < TEXT_Y ;i++){
    for(j = 0; j < TEXT_X;j++){
        if(mask[ j + i * TEXT_X] !=0){
            fruit_text[j + i * TEXT_X] = fruit_text[j + i * TEXT_X] + offset_color; //update the color of masked pixel with the pragrant color stored 0x40 after the origin color.
        }
    }
  }
}
